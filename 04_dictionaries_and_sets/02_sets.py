# Sets are similar to dictionaries but they are accessed by a key
# Its unordered and does not contains duplicates
# Two ways to create sets
farm_animals = {"sheep", "cow", "hen"}

print(farm_animals)

for animal in farm_animals:
    print(animal)

print("=" * 40)

wild_animals = set(['lion', 'tiger', 'panther'])

for animal in wild_animals:
    print(animal)

